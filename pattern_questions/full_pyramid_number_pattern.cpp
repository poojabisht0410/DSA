#include<iostream>
using namespace std;




int main()
{
    int N;
    cin>>N;

    for(int i=i;i<=N;i++){

        for(int j=N-i;j>0;j--){
            cout<<" ";
        }
        
        for(int j=i;j<=i;j++){
            cout<<j<<j+1;
        }
        cout<<endl;
    }
return 0;
}